interface LanguageProviderProps {
  children: ReactNode;
}

export const LanguageProvider: React.FC<LanguageProviderProps> = ({ children }) => {
  // Initialize from localStorage, fallback to 'tr' (Turkish default)
  const [language, setLanguage] = useState<'en' | 'tr'>(() => {
    const saved = localStorage.getItem('lotastro-language');
    return (saved === 'en' || saved === 'tr') ? saved : 'tr';
  });

  // Persist to localStorage whenever language changes
  useEffect(() => {
    localStorage.setItem('lotastro-language', language);
    document.documentElement.lang = language;
  }, [language]);

  const t = (key: string, params?: Record<string, string | number>): string | string[] => {
    // Try nested key resolution first (e.g., "aiOrder.title" -> translations[language].aiOrder.title)
    let result: any;
    
    if (key.includes('.')) {
      const keys = key.split('.');
      let value: any = translations[language];
      
      for (const k of keys) {
        if (value && typeof value === 'object' && k in value) {
          value = value[k];
        } else {
          value = undefined;
          break;
        }
      }
      
      if (value !== undefined) {
        result = value;
      }
    }
    
    // Fallback to flat key lookup for backward compatibility
    if (result === undefined) {
      result = translations[language][key];
    }
    
    // Missing key detection
    if (result === undefined) {
      if (process.env.NODE_ENV === 'development') {
        console.warn('[i18n] Missing translation key:', key);
      }
      return `{{missing:${key}}}`;
    }
    
    // Replace placeholders with params if provided
    if (typeof result === 'string' && params) {
      for (const [paramKey, paramValue] of Object.entries(params)) {
        result = result.replace(new RegExp(`\\{\\{${paramKey}\\}\\}`, 'g'), String(paramValue));
      }
    }
    
    return result;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};
